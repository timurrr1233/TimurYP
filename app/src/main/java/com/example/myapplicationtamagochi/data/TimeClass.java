package com.example.myapplicationtamagochi.data;

import java.io.Serializable;

public class TimeClass {
    private int id;
    private int seconds;

    public TimeClass() {
        this.seconds = seconds;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getSeconds() {
        return seconds;
    }

    public void setSeconds(int seconds) {
        this.seconds = seconds;
    }
}
